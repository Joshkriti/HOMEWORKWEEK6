package midweekhomework;

public class Question_2 {

    // 2. Print multiplication table of 24, 50 and 29 using loop.

    public static void main(String[] args) {
        // Creating loop to print out multiplication table

        for (int i = 1; i <= 10; i++){

            System.out.print("50 * " + i + " = " + (50*i));
            System.out.print("     29 * " + i + " = " + (29*i));
            System.out.println("     24 * " + i + " = " +(24*i));
        }



    }


}
